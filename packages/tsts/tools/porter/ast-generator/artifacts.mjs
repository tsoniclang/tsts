// Deterministic digest of each declared schema input, so porter:ast --check can
// fail with a useful diagnostic when a specific input changes.
function schemaInputDigests(schema) {
  return schema.inputs.map((input) => ({
    path: input.path,
    contentHash: hashText(input.text),
  }));
}

function generatedFile(relativePath, body, sourceRevision, schemaInputs) {
  return renderGeneratedArtifactEnvelope({
    body,
    header: "// Code generated by TSTS AST generator. DO NOT EDIT.",
    kind: GENERATED_KIND,
    path: relativePath,
    providerId: GENERATOR,
    schemaInputs,
    sourceRevision,
  });
}

// Returns Map<tsRootRelativePath, fullFileText>.
export function buildAstGeneratedFiles(config, sourceRevision) {
  const ac = astConfig(config);
  const schema = loadAstSchema(config);
  const model = schema.model;
  assertNodeDataMethodsMatchUpstream(schema.nodeDataSource);
  assertProtocolGeneratedSourceMatchesSchema(schema.protocolGeneratedSource, model);
  const schemaInputs = schemaInputDigests(schema);
  const files = new Map();
  for (const emitter of EMITTERS) {
    const relativePath = `${ac.generatedDir}/${emitter.file}`;
    const body = emitter.model ? emitter.emit(model) : emitter.emit(schema);
    files.set(relativePath, generatedFile(relativePath, body, sourceRevision, schemaInputs));
  }
  return files;
}

export function buildAstGeneratedTypeOwnership(config, snapshot) {
  const ac = astConfig(config);
  const model = loadAstSchema(config).model;
  const routes = astGeneratedTypeRoutes(ac.generatedDir, model);
  const sourcePaths = new Set(["internal/ast/ast_generated.go", "internal/ast/kind_generated.go"]);
  const units = (snapshot.files ?? [])
    .filter((file) => sourcePaths.has(file.path))
    .flatMap((file) => (file.units ?? []).filter((unit) => unit.kind === "type"));
  const unitNames = new Set(units.map((unit) => unit.name));
  const missing = [...unitNames].filter((name) => !routes.has(name)).sort();
  const extra = [...routes.keys()].filter((name) => !unitNames.has(name)).sort();
  if (missing.length > 0 || extra.length > 0) {
    throw new Error(`AST generated type ownership differs from pinned Go declarations (missing routes: ${missing.join(", ") || "none"}; extra routes: ${extra.join(", ") || "none"})`);
  }
  return units.map((unit) => ({
    generator: GENERATOR,
    objectId: exactSemanticTypeObjectId(unit),
    unitId: unit.id,
    moduleId: `${config.tsRoot.replace(/\/$/, "")}/${routes.get(unit.name)}`,
    tsName: unit.name,
  })).sort((left, right) => compareOwnership(left, right));
}

function astGeneratedTypeRoutes(generatedDir, model) {
  const routes = new Map();
  const add = (name, file) => {
    if (routes.has(name)) throw new Error(`AST generator assigns Go type '${name}' to more than one TypeScript module`);
    routes.set(name, `${generatedDir}/${file}`);
  };
  add("Kind", "kinds.ts");
  for (const name of model.kindAliasNames()) add(name, "kinds.ts");
  add("NodeFactory", "factory.ts");
  for (const name of model.baseNames()) if (!HAND_WRITTEN_BASES.has(name)) add(name, "node.ts");
  for (const name of model.nodeNames()) {
    add(`${name}Node`, "unions.ts");
    if (!model.definitions[name].handWritten) add(name, "data.ts");
    for (const alias of model.instantiationAliasesOf(name)) add(alias.name, "unions.ts");
  }
  for (const name of Object.keys(model.listAliases)) add(name, "unions.ts");
  for (const name of Object.keys(model.aliases)) add(name, "unions.ts");
  return routes;
}

function exactSemanticTypeObjectId(unit) {
  const objectIds = new Set((unit.semantic ?? []).map((variant) => variant?.type?.object?.id).filter((id) => typeof id === "string" && id !== ""));
  if (objectIds.size !== 1) throw new Error(`generated Go type '${unit.id}' must have one profile-invariant object identity`);
  return [...objectIds][0];
}

function compareOwnership(left, right) {
  return left.objectId.localeCompare(right.objectId) || left.moduleId.localeCompare(right.moduleId) || left.tsName.localeCompare(right.tsName);
}

export function assertProtocolGeneratedMatchesSchema(config) {
  const schema = loadAstSchema(config);
  assertProtocolGeneratedSourceMatchesSchema(schema.protocolGeneratedSource, schema.model);
}

function assertProtocolGeneratedSourceMatchesSchema(source, model) {
  const actualChildren = parseProtocolPropertyMap(source, "childProperties", true);
  const actualSingle = parseProtocolPropertyMap(source, "singleChildNodePropertyNames", false);
  const expectedChildren = new Map();
  for (const nodeName of model.nodeNames()) {
    const children = model.schemaMembers(nodeName)
      .filter((member) => member.isChild() && !member.noTS)
      .map((member) => model.uncapitalize(member.name));
    if (children.length === 0) continue;
    const kindTypes = model.kindTypesOf(nodeName).kindNames;
    const kindNames = new Set([...kindTypes, ...model.kindAliasesOf(nodeName)]);
    if (kindTypes.length === 1) kindNames.add(model.syntaxKindName(nodeName));
    for (const kindName of kindNames) expectedChildren.set(kindName, children);
  }
  const expectedSingle = new Map([...expectedChildren].filter(([, children]) => children.length === 1).map(([kind, children]) => [kind, children[0]]));
  const differences = [
    ...compareProtocolMap("childProperties", expectedChildren, actualChildren),
    ...compareProtocolMap("singleChildNodePropertyNames", expectedSingle, actualSingle),
  ];
  if (differences.length > 0) {
    throw new Error(`native-preview protocol child ordering differs from the AST schema: ${differences.slice(0, 8).join("; ")}`);
  }
}

function parseProtocolPropertyMap(source, variableName, arrays) {
  const start = source.indexOf(`export const ${variableName}`);
  if (start < 0) throw new Error(`protocol generated input is missing ${variableName}`);
  const bodyStart = source.indexOf("{", start);
  const bodyEnd = source.indexOf("\n};", bodyStart);
  if (bodyStart < 0 || bodyEnd < 0) throw new Error(`protocol generated input has malformed ${variableName}`);
  const body = source.slice(bodyStart + 1, bodyEnd);
  const result = new Map();
  const expression = arrays
    ? /\[SyntaxKind\.([A-Za-z0-9_]+)\]:\s*\[([^\]]*)\]/g
    : /\[SyntaxKind\.([A-Za-z0-9_]+)\]:\s*"([^"]+)"/g;
  for (const match of body.matchAll(expression)) {
    const value = arrays
      ? [...match[2].matchAll(/"([^"]+)"/g)].map((item) => item[1])
      : match[2];
    result.set(match[1], value);
  }
  return result;
}

function compareProtocolMap(label, expected, actual) {
  const differences = [];
  for (const [kind, value] of expected) {
    if (!actual.has(kind)) differences.push(`${label}.${kind} missing`);
    else if (JSON.stringify(actual.get(kind)) !== JSON.stringify(value)) differences.push(`${label}.${kind} expected ${JSON.stringify(value)}, got ${JSON.stringify(actual.get(kind))}`);
  }
  for (const kind of actual.keys()) {
    if (!expected.has(kind)) differences.push(`${label}.${kind} is unexpected`);
  }
  return differences;
}

// Returns { missing, stale, orphan, untracked, invalid } — same shape as facades.
export function buildAstGeneratedArtifactStatus(config, sourceRevision) {
  const ac = astConfig(config);
  const expected = buildAstGeneratedFiles(config, sourceRevision);
  const expectedPaths = new Set(expected.keys());
  const inventory = inventoryGeneratedArtifactsForProvider(resolveRepo(ac.tsRoot), GENERATOR);
  const actualFiles = inventory.files;
  const actualPaths = new Set(actualFiles);

  const missing = [];
  const stale = [];
  const orphan = [];
  const untracked = [];
  const invalid = inventory.invalid.map((entry) => ({ ...entry }));

  for (const relativePath of [...expectedPaths].sort()) {
    if (!actualPaths.has(relativePath)) {
      missing.push({ path: relativePath, reason: "Expected generated AST artifact is missing." });
    }
  }

  for (const relativePath of actualFiles) {
    const absolute = resolveRepo(path.join(ac.tsRoot, relativePath));
    const text = readFileSync(absolute, "utf8");
    const registration = inspectGeneratedArtifactRegistration(relativePath, text);
    if (registration.error !== undefined) {
      invalid.push({ path: relativePath, reason: registration.error });
      continue;
    }
    if (registration.metadata === undefined) {
      untracked.push({ path: relativePath, reason: "Generated AST directory file is missing @tsgo-generated metadata." });
      continue;
    }
    if (registration.provider?.id !== GENERATOR) {
      invalid.push({ path: relativePath, reason: `generated artifact path is registered to '${registration.provider?.id}' instead of '${GENERATOR}'` });
      continue;
    }
    if (!expectedPaths.has(relativePath)) {
      orphan.push({ path: relativePath, reason: "Generated AST file is no longer produced from the current schema." });
      continue;
    }
    if (text !== expected.get(relativePath)) {
      stale.push({ path: relativePath, reason: "Generated AST file differs from the current deterministic generator output." });
    }
  }

  return { missing, stale, orphan, untracked, invalid };
}

export function collectAstArtifactFailures(status) {
  const failures = [];
  if (status.missing.length > 0) failures.push(`${status.missing.length} missing AST generated artifacts`);
  if (status.stale.length > 0) failures.push(`${status.stale.length} stale AST generated artifacts`);
  if (status.orphan.length > 0) failures.push(`${status.orphan.length} orphan AST generated artifacts`);
  if (status.untracked.length > 0) failures.push(`${status.untracked.length} untracked AST generated artifacts`);
  if (status.invalid.length > 0) failures.push(`${status.invalid.length} invalid AST generated artifacts`);
  return failures;
}

export function writeAstGenerated(config, sourceRevision, options = {}) {
  const ac = astConfig(config);
  const expected = buildAstGeneratedFiles(config, sourceRevision);
  const results = [];
  for (const [relativePath, text] of expected) {
    const absolute = resolveRepo(path.join(ac.tsRoot, relativePath));
    const outcome = writeTextSafely(absolute, text, { force: options.force === true, label: "AST generated artifact" });
    results.push({ path: relativePath, outcome });
  }
  return results;
}

export function emptyAstGeneratedArtifactStatus() {
  return { missing: [], stale: [], orphan: [], untracked: [], invalid: [] };
}
