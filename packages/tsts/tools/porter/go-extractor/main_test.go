func TestScanGoFileUsesGoGeneratedMarkerContract(t *testing.T) {
	tests := []struct {
		name   string
		source string
		want   bool
	}{
		{name: "canonical", source: "// Code generated by tool; DO NOT EDIT.\npackage sample\n", want: true},
		{name: "crlf", source: "// Code generated by tool; DO NOT EDIT.\r\npackage sample\r\n", want: true},
		{name: "generator source literal", source: "package main\nfunc emit() { write(\"// Code generated by tool; DO NOT EDIT.\\n\") }\n"},
		{name: "generator source raw literal", source: "package main\nconst marker = `\n// Code generated by tool; DO NOT EDIT.\n`\n"},
		{name: "two unrelated fragments", source: "// Code generated appears here\n// DO NOT EDIT appears there\npackage sample\n"},
		{name: "indented marker accepted by go/ast", source: "  // Code generated by tool; DO NOT EDIT.\npackage sample\n", want: true},
		{name: "missing period", source: "// Code generated by tool; DO NOT EDIT\npackage sample\n"},
	}
	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			root := t.TempDir()
			file := filepath.Join(root, "source.go")
			if err := os.WriteFile(file, []byte(test.source), 0o644); err != nil {
				t.Fatal(err)
			}
			if got := scanGoFile(root, file, "example/source").Generated; got != test.want {
				t.Fatalf("Generated = %v, want %v", got, test.want)
			}
		})
	}
}

func TestShouldSkipNestedGitCheckout(t *testing.T) {
	root := t.TempDir()
	nested := filepath.Join(root, "_submodules", "TypeScript")
	if err := os.MkdirAll(nested, 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(filepath.Join(nested, ".git"), []byte("gitdir: elsewhere\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	if !shouldSkipDir("TypeScript", nested, root) {
		t.Fatal("nested Git checkout must be excluded from the Go source walk")
	}
	ordinary := filepath.Join(root, "internal")
	if err := os.MkdirAll(ordinary, 0o755); err != nil {
		t.Fatal(err)
	}
	if shouldSkipDir("internal", ordinary, root) {
		t.Fatal("ordinary source directory must remain in the Go source walk")
	}
}

