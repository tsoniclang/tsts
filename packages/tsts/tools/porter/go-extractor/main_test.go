func TestConstantEvaluatorMatchesGoTypesForTypedConversions(t *testing.T) {
	for _, expression := range []string{
		"^uint8(1)",
		"^int16(1)",
		"float32(1.0 / 3.0)",
		"uint32(3) << 4",
	} {
		t.Run(expression, func(t *testing.T) {
			parsed, err := parser.ParseExpr(expression)
			if err != nil {
				t.Fatal(err)
			}
			actual, reason := evaluateConstantExpression(parsed, constant.MakeInt64(0), map[string]constant.Value{})
			if actual == nil {
				t.Fatalf("extractor rejected supported expression: %s", reason)
			}
			expected, err := types.Eval(token.NewFileSet(), types.NewPackage("example/test", "test"), token.NoPos, expression)
			if err != nil {
				t.Fatal(err)
			}
			if actual.Kind() != expected.Value.Kind() || actual.ExactString() != expected.Value.ExactString() {
				t.Fatalf("extractor = %s %s, go/types = %s %s", actual.Kind(), actual.ExactString(), expected.Value.Kind(), expected.Value.ExactString())
			}
		})
	}
	parsed, err := parser.ParseExpr("uint8(256)")
	if err != nil {
		t.Fatal(err)
	}
	if value, reason := evaluateConstantExpression(parsed, constant.MakeInt64(0), map[string]constant.Value{}); value != nil || !strings.Contains(reason, "overflows") {
		t.Fatalf("overflowing conversion must fail closed, got %v, %q", value, reason)
	}
}

