test("authoredFacadeModules require the exact public symbol while remaining excluded from generation", async () => {
  const root = makePorterTestTemp("porter-test-");
  try {
    const config = authoredWriterConfig(root);
    const snapshot = authoredWriterSnapshot();
    const generatedRoot = path.join(root, "src/go");
    mkdirSync(generatedRoot, { recursive: true });
    writeFileSync(path.join(generatedRoot, "io.ts"), "export interface Writer { Write(): void; }\n");
    const facades = await prepareExternalFacadeStorageCatalog(config, snapshot, repoRoot);
    assert.throws(() => facades.artifactFacades({ ...config }, snapshot), /different config or snapshot/);
    assert.throws(() => facades.artifactFacades(config, structuredClone(snapshot)), /different config or snapshot/);
    assert.throws(() => { config.tsRoot = "changed"; }, TypeError);
    assert.throws(() => snapshot.files.push({}), TypeError);

    // The policy resolves "go/io.ts" to a full repo-relative path.
    assert.ok(authoredFacadePathSet(config).has(`${config.tsRoot}/go/io.ts`));

    // io.ts is excluded from the deterministic generated set (porter:facades will not regenerate it).
    const expected = renderExpectedGeneratedArtifacts(config, snapshot, facades);
    assert.ok(!expected.has(`${config.tsRoot}/go/io.ts`));

    for (const [relativePath, text] of expected) {
      const targetPath = path.join(repoRoot, relativePath);
      mkdirSync(path.dirname(targetPath), { recursive: true });
      writeFileSync(targetPath, text);
    }

    // Authored declaration validity belongs to finalized catalog construction, not generated-artifact status.
    writeFileSync(path.join(generatedRoot, "io.ts"), "export const Other = 1;\n");
    await assert.rejects(
      prepareExternalFacadeStorageCatalog(config, snapshot, repoRoot),
      /does not resolve to an indexed type declaration/,
    );
    assert.deepEqual(buildGeneratedArtifactStatus(config, snapshot, facades), { missing: [], stale: [], orphan: [], untracked: [], invalid: [] });

    writeFileSync(path.join(generatedRoot, "io.ts"), "export interface Writer { Write(): void; }\n");
    assert.deepEqual(buildGeneratedArtifactStatus(config, snapshot, facades), { missing: [], stale: [], orphan: [], untracked: [], invalid: [] });

    // An authored module that still carries @tsgo-generated metadata is invalid (never both).
    writeFileSync(
      path.join(generatedRoot, "io.ts"),
      "// Code generated by TSTS porter. DO NOT EDIT.\n// @tsgo-generated {\"schemaVersion\":1,\"kind\":\"go-facade\",\"generator\":\"porter:facades\",\"path\":\"go/io.ts\",\"sourceRevision\":\"aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa\",\"contentHash\":\"x\"}\n\nexport {}\n",
    );
    const conflicted = buildGeneratedArtifactStatus(config, snapshot, facades);
    assert.equal(conflicted.invalid.length, 1);
    assert.match(conflicted.invalid[0].reason, /Authored facade module must not carry @tsgo-generated/);
  } finally {
    rmSync(root, { recursive: true, force: true });
  }
});

function authoredWriterConfig(root) {
  const tsRoot = path.relative(repoRoot, path.join(root, "src")).split(path.sep).join("/");
  return {
    ...baseConfig,
    tsRoot,
    signatureCheck: {
      facadeTemplate: `${tsRoot}/go/{importPath}.ts`,
      modules: {
        compat: `${tsRoot}/go/compat.ts`,
        core: `${tsRoot}/go/scalars.ts`,
      },
    },
    authoredFacadeModules: ["go/io.ts"],
    externalFacadePolicies: [{ objectId: "io::type::Writer", tsModule: "go/io.ts", tsName: "Writer", storageStrategy: "authored" }],
  };
}

