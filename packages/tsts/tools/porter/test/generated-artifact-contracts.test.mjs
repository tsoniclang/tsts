function unicodeArtifact(artifactPath, body, extra = {}) {
  const metadata = {
    schemaVersion: 1,
    kind: "unicode-generated",
    generator: "porter:unicode",
    sourceRevision: UNICODE_VERSION,
    path: artifactPath,
    contentHash: createHash("sha256").update(body).digest("hex"),
    ...extra,
  };
  return `// Code generated by TSTS unicode generator. DO NOT EDIT.\n// @tsgo-generated ${JSON.stringify(metadata)}\n\n${body}`;
}

