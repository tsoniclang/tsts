test("buildGeneratedArtifactStatus catches missing, stale, orphan, untracked, and invalid generated files", () => {
  const root = makePorterTestTemp("porter-test-");
  try {
    const config = {
      ...baseConfig,
      tsRoot: path.relative(repoRoot, path.join(root, "src")).split(path.sep).join("/"),
      tsFilePolicies: [
        { match: "**/*.test.ts", category: "test-parity", reason: "test parity" },
        ...baseConfig.tsFilePolicies,
      ],
    };
    const snapshot = snapshotWith([]);
    const facades = finalizeGeneratedFacadeFixtureCatalog(config, snapshot);
    const generatedRoot = path.join(root, "src/go");
    mkdirSync(generatedRoot, { recursive: true });
    for (const [relativePath, text] of renderExpectedGeneratedArtifacts(config, snapshot, facades)) {
      const targetPath = path.join(repoRoot, relativePath);
      mkdirSync(path.dirname(targetPath), { recursive: true });
      writeFileSync(targetPath, text);
    }
    let clean = buildGeneratedArtifactStatus(config, snapshot, facades);
    assert.deepEqual(clean, { missing: [], stale: [], orphan: [], untracked: [], invalid: [] });

    const compatArtifact = renderExpectedGeneratedArtifacts(config, snapshot, facades).get(`${config.tsRoot}/go/compat.ts`);
    writeFileSync(path.join(generatedRoot, "hidden.test.ts"), compatArtifact.replace('"path":"go/compat.ts"', '"path":"go/hidden.test.ts"'));
    for (const directory of [".git", "dist", "node_modules"]) {
      mkdirSync(path.join(generatedRoot, directory), { recursive: true });
      writeFileSync(
        path.join(generatedRoot, directory, "hidden.ts"),
        compatArtifact.replace('"path":"go/compat.ts"', `"path":"go/${directory}/hidden.ts"`),
      );
    }
    const forged = buildGeneratedArtifactStatus(config, snapshot, facades);
    assert.deepEqual(forged.invalid.map((issue) => issue.path), [`${config.tsRoot}/go/hidden.test.ts`]);
    assert.deepEqual(forged.orphan.map((issue) => issue.path), [
      `${config.tsRoot}/go/.git/hidden.ts`,
      `${config.tsRoot}/go/dist/hidden.ts`,
      `${config.tsRoot}/go/node_modules/hidden.ts`,
    ]);

    writeFileSync(
      path.join(generatedRoot, "compat.ts"),
      renderExpectedGeneratedArtifacts(config, snapshot, facades).get(`${config.tsRoot}/go/compat.ts`).replace(/\n$/, "\nexport const edited = true;\n"),
    );
    writeFileSync(path.join(generatedRoot, "manual.ts"), "export const manual = true;\n");
    writeFileSync(path.join(generatedRoot, "bad.ts"), "// Code generated by TSTS porter. DO NOT EDIT.\n// @tsgo-generated {bad-json}\n\nexport {}\n");
    const orphan = renderExpectedGeneratedArtifacts(config, snapshot, facades).get(`${config.tsRoot}/go/compat.ts`).replace('"path":"go/compat.ts"', '"path":"go/orphan.ts"');
    writeFileSync(path.join(generatedRoot, "orphan.ts"), orphan);
    rmSync(path.join(generatedRoot, "scalars.ts"), { force: true });

    const broken = buildGeneratedArtifactStatus(config, snapshot, facades);
    assert.equal(broken.missing.length, 1);
    assert.equal(broken.stale.length, 1);
    assert.equal(broken.orphan.length, 4);
    assert.equal(broken.untracked.length, 1);
    assert.equal(broken.invalid.length, 2);
  } finally {
    rmSync(root, { recursive: true, force: true });
  }
});

test("bundled-generator catches missing, stale, orphan, untracked, and invalid generated files", () => {
  const root = makePorterTestTemp("bundled-test-");
  try {
    const sourceRoot = path.relative(repoRoot, path.join(root, "vendor")).split(path.sep).join("/");
    const tsRoot = path.relative(repoRoot, path.join(root, "src")).split(path.sep).join("/");
    const libSource = path.join(root, "vendor/internal/bundled/libs");
    mkdirSync(libSource, { recursive: true });
    mkdirSync(path.join(root, "src"), { recursive: true });
    writeFileSync(path.join(libSource, "lib.one.d.ts"), "declare const one: 1;\n");
    const config = { ...baseConfig, sourceRoot, tsRoot };

    const missing = buildBundledGeneratedArtifactStatus(config, "rev");
    assert.deepEqual(collectBundledArtifactFailures(missing), ["3 missing bundled artifacts"]);
    assert.equal(writeBundledGenerated(config, "rev"), 3);

    const clean = buildBundledGeneratedArtifactStatus(config, "rev");
    assert.deepEqual(clean, { missing: [], stale: [], orphan: [], untracked: [], invalid: [] });
    const expected = buildExpectedBundledArtifacts(config, "rev");
    assert.match(expected.get(`${tsRoot}/internal/bundled/libs_generated.ts`), /"path":"internal\/bundled\/libs_generated\.ts"/);

    writeFileSync(
      path.join(root, "src/internal/bundled/embed_generated.ts"),
      expected.get(`${tsRoot}/internal/bundled/embed_generated.ts`).replace(/\["libs\/lib\.one\.d\.ts", \d+\]/, '["libs/lib.one.d.ts", 999]'),
    );
    const stale = buildBundledGeneratedArtifactStatus(config, "rev");
    assert.equal(stale.stale.length, 1);

    writeBundledGenerated(config, "rev");
    unlinkSync(path.join(root, "src/internal/bundled/libs/lib.one.d.ts"));
    const missingLib = buildBundledGeneratedArtifactStatus(config, "rev");
    assert.equal(missingLib.missing.length, 1);

    writeBundledGenerated(config, "rev");
    writeFileSync(path.join(root, "src/internal/bundled/libs/extra.d.ts"), "not generated\n");
    const untracked = buildBundledGeneratedArtifactStatus(config, "rev");
    assert.equal(untracked.untracked.length, 1);

    writeBundledGenerated(config, "rev");
    writeFileSync(
      path.join(root, "src/internal/bundled/libs/extra.d.ts"),
      `// Code generated by TSTS bundled generator. DO NOT EDIT.\n// @tsgo-generated {"schemaVersion":1,"kind":"bundled-generated","generator":"porter:bundled","sourceRevision":"rev","path":"internal/bundled/libs/extra.d.ts","contentHash":"${"a".repeat(64)}"}\n\n`,
    );
    const orphan = buildBundledGeneratedArtifactStatus(config, "rev");
    assert.equal(orphan.orphan.length, 1);

    writeBundledGenerated(config, "rev");
    const directOrphanPath = path.join(root, "src/internal/bundled/old_generated.ts");
    writeFileSync(
      directOrphanPath,
      `// Code generated by TSTS bundled generator. DO NOT EDIT.\n// @tsgo-generated {"schemaVersion":1,"kind":"bundled-generated","generator":"porter:bundled","sourceRevision":"rev","path":"internal/bundled/old_generated.ts","contentHash":"${"a".repeat(64)}"}\n\n`,
    );
    const directOrphan = buildBundledGeneratedArtifactStatus(config, "rev");
    assert.deepEqual(directOrphan.orphan.map((issue) => issue.path), [`${tsRoot}/internal/bundled/old_generated.ts`]);
    unlinkSync(directOrphanPath);

    writeBundledGenerated(config, "rev");
    writeFileSync(path.join(root, "src/internal/bundled/libs/extra.d.ts"), "// @tsgo-generated {bad-json}\n");
    const invalid = buildBundledGeneratedArtifactStatus(config, "rev");
    assert.equal(invalid.invalid.length, 1);
  } finally {
    rmSync(root, { force: true, recursive: true });
  }
});

test("unicode-generator classifies every file in its registered generated root", () => {
  const root = makePorterTestTemp("unicode-artifact-test-");
  try {
    const tsRoot = path.relative(repoRoot, path.join(root, "src")).split(path.sep).join("/");
    const generatedRoot = path.join(root, "src/internal/stringutil/generated");
    mkdirSync(generatedRoot, { recursive: true });
    assert.equal(buildUnicodeGeneratedArtifactStatus({ tsRoot }).missing.length, 2);

    for (const name of ["identifier_parts_generated.ts", "js_case_generated.ts"]) {
      const artifactPath = `internal/stringutil/generated/${name}`;
      writeFileSync(path.join(generatedRoot, name), unicodeArtifact(artifactPath, "export {};\n"));
    }
    assert.deepEqual(buildUnicodeGeneratedArtifactStatus({ tsRoot }), { missing: [], stale: [], orphan: [], untracked: [], invalid: [] });

    writeFileSync(path.join(generatedRoot, "obsolete.ts"), unicodeArtifact("internal/stringutil/generated/obsolete.ts", "export {};\n"));
    const orphan = buildUnicodeGeneratedArtifactStatus({ tsRoot });
    assert.deepEqual(orphan.orphan.map((issue) => issue.path), [`${tsRoot}/internal/stringutil/generated/obsolete.ts`]);

    writeFileSync(path.join(generatedRoot, "obsolete.ts"), "export {};\n");
    const untracked = buildUnicodeGeneratedArtifactStatus({ tsRoot });
    assert.deepEqual(untracked.untracked.map((issue) => issue.path), [`${tsRoot}/internal/stringutil/generated/obsolete.ts`]);

    writeFileSync(path.join(generatedRoot, "obsolete.ts"), unicodeArtifact("internal/stringutil/generated/obsolete.ts", "export {};\n", { extra: true }));
    const invalid = buildUnicodeGeneratedArtifactStatus({ tsRoot });
    assert.deepEqual(invalid.invalid.map((issue) => issue.path), [`${tsRoot}/internal/stringutil/generated/obsolete.ts`]);
  } finally {
    rmSync(root, { recursive: true, force: true });
  }
});

