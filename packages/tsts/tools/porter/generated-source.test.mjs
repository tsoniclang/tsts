test("generated-source coverage becomes stale when artifact-owned declarations change", (t) => {
  const root = mkdtempSync(path.join(tmpdir(), "tsts-generated-coverage-"));
  t.after(() => rmSync(root, { recursive: true, force: true }));
  const config = { generatedSourceCoveragePath: "coverage.json" };
  const original = snapshotWithAstUnits([unit("A", "sig-a")]);
  writeFileSync(path.join(root, "coverage.json"), renderGeneratedSourceCoverage(original));
  assert.deepEqual(buildGeneratedSourceCoverageStatus(root, config, original).issues, []);

  const withHiddenBehavior = snapshotWithAstUnits([
    unit("A", "sig-a"),
    unit("NewDeclaration", "sig-b"),
  ]);
  assert.match(buildGeneratedSourceCoverageStatus(root, config, withHiddenBehavior).issues[0].reason, /stale/);
});

test("global generated-artifact registry reports uncovered registrations without duplicating provider defects", (t) => {
  const root = mkdtempSync(path.join(tmpdir(), "tsts-generated-artifacts-"));
  t.after(() => rmSync(root, { recursive: true, force: true }));
  const tsRoot = path.join(root, "src");
  mkdirSync(path.join(tsRoot, "unknown"), { recursive: true });
  mkdirSync(path.join(tsRoot, "go"), { recursive: true });
  mkdirSync(path.join(tsRoot, "internal/ast/generated"), { recursive: true });
  writeFileSync(path.join(tsRoot, "unknown", "output.ts"), [
    "// Code generated. DO NOT EDIT.",
    `// @tsgo-generated {"schemaVersion":1,"kind":"ast-generated","generator":"porter:ast","sourceRevision":"rev","path":"unknown/output.ts","contentHash":"${"a".repeat(64)}"}`,
    "",
    "",
  ].join("\n"));
  writeFileSync(path.join(tsRoot, "go", "compat.ts"), [
    "// Code generated. DO NOT EDIT.",
    `// @tsgo-generated {"schemaVersion":1,"kind":"go-compat","generator":"wrong-generator","sourceRevision":"rev","path":"go/compat.ts","contentHash":"${"a".repeat(64)}"}`,
    "",
    "export const hidden = undefined as never;",
  ].join("\n"));
  writeFileSync(path.join(tsRoot, "internal/ast/generated/bad.ts"), [
    "// Code generated. DO NOT EDIT.",
    "// @tsgo-generated {bad-json}",
    "",
    "",
  ].join("\n"));
  writeFileSync(path.join(tsRoot, "unknown", "header-only.ts"), "// Code generated by an unknown tool. DO NOT EDIT.\nexport {};\n");
  const status = buildGlobalGeneratedArtifactStatus(root, { tsRoot: "src" });
  assert.deepEqual(status.issues.map((issue) => issue.path), [
    "go/compat.ts",
    "internal/ast/generated/bad.ts",
    "unknown/header-only.ts",
    "unknown/output.ts",
  ]);
  assert.match(status.issues.find((issue) => issue.path === "go/compat.ts").reason, /does not match provider/);
  assert.match(status.issues.find((issue) => issue.path === "internal/ast/generated/bad.ts").reason, /invalid @tsgo-generated JSON/);
  assert.match(status.issues.find((issue) => issue.path === "unknown/header-only.ts").reason, /has no exact @tsgo-generated registration/);
  assert.match(status.issues.find((issue) => issue.path === "unknown/output.ts").reason, /has no registered provider/);
});

test("generated artifact registration is exact and rejects duplicate ownership records", () => {
  const exactMetadata = {
    schemaVersion: 1,
    kind: "ast-generated",
    generator: "porter:ast",
    sourceRevision: "rev",
    path: "internal/ast/generated/types.ts",
    contentHash: "a".repeat(64),
    schemaInputs: [{ path: "schema/ast.json", contentHash: "b".repeat(64) }],
  };
  const metadata = generatedRecord(exactMetadata);
  const header = "// Code generated. DO NOT EDIT.";
  const exact = inspectGeneratedArtifactRegistration("internal/ast/generated/types.ts", `${header}\n${metadata}\n\nexport type Node = unknown;\n`);
  assert.equal(exact.provider.id, "porter:ast");
  assert.equal(exact.error, undefined);
  assert.match(inspectGeneratedArtifactRegistration("internal/ast/generated/types.ts", `${header}\n${metadata}\n${metadata}\n`).error, /exactly one/);
  assert.match(inspectGeneratedArtifactRegistration("internal/ast/generated/types.ts", `${metadata}\n`).error, /second line/);
  const malformed = inspectGeneratedArtifactRegistration("internal/ast/generated/types.ts", `${header}\n// @tsgo-generated {not-json}\n\n`);
  assert.equal(malformed.provider.id, "porter:ast");
  assert.match(malformed.error, /invalid @tsgo-generated JSON/);
  assert.match(inspectGeneratedArtifactRegistration("internal/ast/generated/other.ts", `${header}\n${metadata}\n\n`).error, /metadata path/);
  assert.match(inspectGeneratedArtifactRegistration("internal/ast/generated/types.ts", `${header}\n${generatedRecord({ ...exactMetadata, extra: true })}\n\n`).error, /keys must be exactly/);
  assert.match(inspectGeneratedArtifactRegistration("internal/ast/generated/types.ts", `${header}\n${generatedRecord({ ...exactMetadata, contentHash: "short" })}\n\n`).error, /lowercase SHA-256/);
  assert.match(inspectGeneratedArtifactRegistration("internal/ast/generated/types.ts", `${header}\n${generatedRecord({ ...exactMetadata, schemaInputs: [] })}\n\n`).error, /non-empty array/);
  assert.match(inspectGeneratedArtifactRegistration("internal/ast/generated/types.ts", `${header}\n${generatedRecord({ ...exactMetadata, schemaInputs: [{ path: "../escape", contentHash: "b".repeat(64) }] })}\n\n`).error, /canonical non-empty POSIX/);
  assert.match(inspectGeneratedArtifactRegistration("internal/ast/generated/types.ts", `${header}\n${generatedRecord({ ...exactMetadata, schemaInputs: [{ path: "schema/ast.json", contentHash: "b".repeat(64), extra: true }] })}\n\n`).error, /exactly path and contentHash/);
});

test("one exact envelope renderer covers every registered provider schema", () => {
  const cases = [
    { providerId: "porter:facades", kind: "go-facade", path: "go/example.ts" },
    { providerId: "porter:ast", kind: "ast-generated", path: "internal/ast/generated/example.ts", schemaInputs: [{ path: "schema/ast.json", contentHash: "b".repeat(64) }] },
    { providerId: "porter:diagnostics", kind: "diagnostics-generated", path: "internal/diagnostics/generated/example.ts", inputs: [{ path: "vendor/messages.go", contentHash: "b".repeat(64) }] },
    { providerId: "porter:bundled", kind: "bundled-generated", path: "internal/bundled/libs/example.d.ts" },
    { providerId: "porter:unicode", kind: "unicode-generated", path: "internal/stringutil/generated/example.ts" },
    { providerId: "porter:value-operations", kind: "go-value-operations", path: "internal/go-value-operations/generated/example.ts" },
  ];
  assert.equal(cases.length, generatedArtifactProviders.length);
  assert.equal(new Set(generatedArtifactProviders.map((provider) => provider.id)).size, generatedArtifactProviders.length);
  assert.equal(new Set(generatedArtifactProviders.map((provider) => provider.statusKey)).size, generatedArtifactProviders.length);
  assert.equal(new Set(generatedArtifactProviders.flatMap((provider) => provider.kinds)).size, generatedArtifactProviders.flatMap((provider) => provider.kinds).length);
  for (const row of cases) {
    const text = renderGeneratedArtifactEnvelope({
      body: "export {};\n",
      header: "// Code generated for a contract test. DO NOT EDIT.",
      sourceRevision: "rev",
      ...row,
    });
    assert.equal(inspectGeneratedArtifactRegistration(row.path, text).error, undefined);
    assert.equal(stripGeneratedArtifactEnvelope(text), "export {};\n");
  }
  assert.throws(() => renderGeneratedArtifactEnvelope({
    body: "",
    extra: true,
    header: "// Code generated. DO NOT EDIT.",
    kind: "go-facade",
    path: "go/example.ts",
    providerId: "porter:facades",
    sourceRevision: "rev",
  }), /render input keys must be exactly/);
});

test("generated artifact provider discovery covers every registered path under its bounded roots", (t) => {
  const root = mkdtempSync(path.join(tmpdir(), "tsts-generated-providers-"));
  t.after(() => rmSync(root, { recursive: true, force: true }));
  mkdirSync(path.join(root, "internal/bundled/libs"), { recursive: true });
  mkdirSync(path.join(root, "internal/stringutil/generated/deep"), { recursive: true });
  writeFileSync(path.join(root, "internal/bundled/old_generated.ts"), "");
  writeFileSync(path.join(root, "internal/bundled/libs/old.d.ts"), "");
  writeFileSync(path.join(root, "internal/bundled/libs/data.bin"), "");
  writeFileSync(path.join(root, "internal/bundled/authored.ts"), "");
  writeFileSync(path.join(root, "internal/stringutil/generated/deep/old.ts"), "");
  writeFileSync(path.join(root, "internal/stringutil/generated/data.bin"), "");
  symlinkSync(path.join(root, "internal/stringutil/generated/data.bin"), path.join(root, "internal/stringutil/generated/linked.ts"));

  const bundled = inventoryGeneratedArtifactsForProvider(root, "porter:bundled");
  assert.deepEqual(bundled.files, [
    "internal/bundled/libs/data.bin",
    "internal/bundled/libs/old.d.ts",
    "internal/bundled/old_generated.ts",
  ]);
  const unicode = inventoryGeneratedArtifactsForProvider(root, "porter:unicode");
  assert.deepEqual(unicode.files, [
    "internal/stringutil/generated/data.bin",
    "internal/stringutil/generated/deep/old.ts",
  ]);
  assert.deepEqual(unicode.invalid, [{
    path: "internal/stringutil/generated/linked.ts",
    reason: "generated artifact path must be a regular file, found symbolic link",
  }]);
  assert.throws(() => inventoryGeneratedArtifactsForProvider("relative", "porter:ast"), /must be absolute/);
  assert.throws(() => inventoryGeneratedArtifactsForProvider(root, "missing"), /unknown generated artifact provider/);
});

test("exact filesystem inventory returns regular files without following symbolic links", (t) => {
  const root = mkdtempSync(path.join(tmpdir(), "tsts-generated-walker-"));
  t.after(() => rmSync(root, { recursive: true, force: true }));
  mkdirSync(path.join(root, "z/deep"), { recursive: true });
  mkdirSync(path.join(root, "a"), { recursive: true });
  writeFileSync(path.join(root, "z/deep/output.ts"), "");
  writeFileSync(path.join(root, "z/types.d.ts"), "");
  writeFileSync(path.join(root, "a/first.ts"), "");
  writeFileSync(path.join(root, "ignored.js"), "");
  symlinkSync(path.join(root, "a"), path.join(root, "linked"));

  assert.deepEqual(
    walkExactRegularFiles(root).map((file) => path.relative(root, file).split(path.sep).join("/")),
    ["a/first.ts", "ignored.js", "z/deep/output.ts", "z/types.d.ts"],
  );
});

test("provider inventory rejects a symbolic-link generated root without following it", (t) => {
  const root = mkdtempSync(path.join(tmpdir(), "tsts-generated-root-link-"));
  t.after(() => rmSync(root, { recursive: true, force: true }));
  mkdirSync(path.join(root, "internal/ast"), { recursive: true });
  mkdirSync(path.join(root, "outside"), { recursive: true });
  writeFileSync(path.join(root, "outside/forged.ts"), "");
  symlinkSync(path.join(root, "outside"), path.join(root, "internal/ast/generated"));
  const inventory = inventoryGeneratedArtifactsForProvider(root, "porter:ast");
  assert.deepEqual(inventory.files, []);
  assert.deepEqual(inventory.invalid, [{
    path: "internal/ast/generated",
    reason: "generated artifact area 'internal/ast/generated' must not be a symbolic link",
  }]);
});

function snapshotWithAstUnits(units) {
  return {
    gitRevision: "a".repeat(40),
    files: [{
      path: "internal/ast/ast_generated.go",
      sourceHash: "source",
      generated: true,
      units,
    }],
  };
}

function unit(name, sigHash) {
  return {
    id: `m::internal/ast/ast_generated.go::func::${name}`,
    kind: "func",
    sigHash,
  };
}

function generatedRecord(metadata) {
  return `// @tsgo-generated ${JSON.stringify(metadata)}`;
}
