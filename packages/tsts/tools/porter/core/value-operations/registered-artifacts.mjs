import path from "node:path";

import { renderGeneratedArtifactEnvelope } from "../../generated-artifact-registry.mjs";
import { compareText } from "../deterministic-order.mjs";

const inputKeys = Object.freeze(["artifacts", "config", "snapshot"]);

export function registerGoValueOperationArtifacts(input) {
  requireExactInput(input, inputKeys, "Go value-operation registration input");
  const { artifacts, config, snapshot } = input;
  if (!(artifacts instanceof Map)) throw new Error("Go value-operation artifacts must be one Map");
  if (typeof snapshot.gitRevision !== "string" || snapshot.gitRevision === "") {
    throw new Error("Go value-operation artifacts require one pinned source revision");
  }
  const tsRoot = canonicalRoot(config.tsRoot);
  const registered = new Map();
  for (const [moduleId, source] of [...artifacts].sort(([left], [right]) => compareText(left, right))) {
    const relativePath = relativeArtifactPath(tsRoot, moduleId);
    if (registered.has(relativePath)) throw new Error(`Go value-operation artifact path '${relativePath}' is duplicated`);
    if (typeof source !== "string") throw new Error(`Go value-operation artifact '${moduleId}' has no source text`);
    const body = source.replace(/\s*$/, "\n");
    registered.set(relativePath, renderGeneratedArtifactEnvelope({
      body,
      header: "// Code generated by TSTS Porter value-operation generator. DO NOT EDIT.",
      kind: "go-value-operations",
      path: relativePath,
      providerId: "porter:value-operations",
      sourceRevision: snapshot.gitRevision,
    }));
  }
  return registered;
}

function canonicalRoot(value) {
  if (typeof value !== "string" || value === "" || value.includes("\\") || path.posix.isAbsolute(value)) {
    throw new Error("Porter tsRoot must be one canonical repository-relative path");
  }
  const normalized = value.replace(/\/+$/, "");
  if (normalized === "" || path.posix.normalize(normalized) !== normalized ||
      normalized.split("/").some((segment) => segment === "" || segment === "." || segment === "..")) {
    throw new Error("Porter tsRoot must be one canonical repository-relative path");
  }
  return normalized;
}

function relativeArtifactPath(tsRoot, moduleId) {
  if (typeof moduleId !== "string" || !moduleId.startsWith(`${tsRoot}/`) || !moduleId.endsWith(".ts") ||
      moduleId.includes("\\") || path.posix.normalize(moduleId) !== moduleId) {
    throw new Error(`Go value-operation module '${moduleId}' must be one canonical TypeScript path under '${tsRoot}'`);
  }
  const relativePath = moduleId.slice(tsRoot.length + 1);
  if (!relativePath.startsWith("internal/go-value-operations/generated/") ||
      relativePath.split("/").some((segment) => segment === "" || segment === "." || segment === "..")) {
    throw new Error(`Go value-operation module '${moduleId}' is outside its registered generated-artifact root`);
  }
  return relativePath;
}

function requireExactInput(value, expectedKeys, label) {
  if (value === null || typeof value !== "object" || Array.isArray(value)) throw new Error(`${label} must be an object`);
  const actual = Object.keys(value).sort(compareText);
  const expected = [...expectedKeys].sort(compareText);
  if (actual.length !== expected.length || actual.some((key, index) => key !== expected[index])) {
    throw new Error(`${label} keys must be exactly ${expected.join(", ")}; got ${actual.join(", ")}`);
  }
  for (const key of expected) if (value[key] === undefined) throw new Error(`${label}.${key} must be defined`);
}
