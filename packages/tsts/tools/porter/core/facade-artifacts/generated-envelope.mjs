import { renderGeneratedArtifactEnvelope, stripGeneratedArtifactEnvelope } from "../../generated-artifact-registry.mjs";

export function renderGeneratedArtifact(snapshot, relativePath, kind, body) {
  const normalizedBody = body.replace(/\s*$/, "\n");
  return renderGeneratedArtifactEnvelope({
    body: normalizedBody,
    header: "// Code generated by TSTS porter. DO NOT EDIT.",
    kind,
    path: relativePath,
    providerId: "porter:facades",
    sourceRevision: snapshot.gitRevision,
  });
}

export function stripGeneratedArtifactHeader(text) {
  return stripGeneratedArtifactEnvelope(text);
}
