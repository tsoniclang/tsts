let embeddedLibRootValue: string | undefined;

function embeddedLibRoot(): string {
  if (embeddedLibRootValue !== undefined) {
    return embeddedLibRootValue;
  }
  const moduleDir = dirname(fileURLToPath(import.meta.url));
  const candidates = [
    join(moduleDir, "libs"),
    resolve(moduleDir, "../../../../src/internal/bundled/libs"),
    resolve(moduleDir, "../../../_vendor/typescript-go/internal/bundled/libs"),
    resolve(moduleDir, "../../../../_vendor/typescript-go/internal/bundled/libs"),
  ];
  for (const candidate of candidates) {
    if (existsSync(join(candidate, "lib.d.ts"))) {
      embeddedLibRootValue = candidate;
      return candidate;
    }
  }
  embeddedLibRootValue = candidates[0]!;
  return embeddedLibRootValue;
}

function hasEmbeddedContent(rest: string): bool {
  return embeddedContentNames.has(rest) as bool;
}

function readEmbeddedContent(rest: string): string | undefined {
  if (!hasEmbeddedContent(rest)) {
    return undefined;
  }
  const cached = embeddedContentCache.get(rest);
  if (cached !== undefined) {
    return cached;
  }
  const content = readFileSync(join(embeddedLibRoot(), rest.replace(/^libs\//, "")), "utf8")
    .replace(generatedBundledHeaderPattern, "");
  embeddedContentCache.set(rest, content);
  return content;
}

function embeddedContentSize(rest: string): long {
  return (embeddedContentSizes.get(rest) ?? 0) as long;
}

